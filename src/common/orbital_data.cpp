void getorb(int iz, int ihole, double xion, int iunf,
            int& norb, int& norbco, int& iorb_out, int& iholep,
            int nqn_out[30], int nk_out[30], double xnel_out[30],
            double xnval_out[30], double xmag_out[30])
{
    if (iz < 1 || iz > 99) {
        std::ostringstream oss;
        oss << " Atomic number " << iz << " not available.";
        logger().wlog(oss.str());
        feff::par::par_stop("GETORB-0");
    }

    int ion = static_cast<int>(std::round(xion));
    double delion = xion - ion;
    // Fortran index = iz - ion (1-based), C++ index0 = iz - ion - 1 (0-based)
    int index0 = iz - ion - 1;
    int ilast = 0;   // 1-based orbital index of last occupied orbital
    int iscr = 0;    // 1-based orbital index for screening electron
    int iion = 0;    // 1-based orbital index for ionization charge adjustment

    iholep = ihole;

    // Find last occupied orbital (ilast) and iion for delion >= 0
    // Fortran loops i=29..1; here we use 0-based: j=28..0, but ilast/iion store 1-based
    for (int j = 28; j >= 0; --j) {
        if (iion == 0 && iocc[index0][j] > delion) iion = j + 1;  // 1-based
        if (ilast == 0 && iocc[index0][j] > 0) ilast = j + 1;     // 1-based
    }

    if (ihole > 0) {
        // ihole is 1-based orbital index
        if (iocc[index0][ihole - 1] < 1) {
            logger().wlog(" Cannot remove an electron from this level");
            feff::par::par_stop("GETORB-1");
        }
    }
    if (ihole == ilast) {
        if (iocc[index0][ihole - 1] - delion < 1) {
            logger().wlog(" Cannot remove an electron from this level");
            feff::par::par_stop("GETORB-1");
        }
    }

    // Find where to put screening electron
    // index1 = index + 1 in Fortran (next element), so index1_0 = index0 + 1
    int index1_0 = index0 + 1;
    for (int j = 0; j < 29; ++j) {
        if (iscr == 0 &&
            (iocc[index1_0][j] - iocc[index0][j]) > 0.5f) {
            iscr = j + 1;  // 1-based
        }
    }

    // Find where to add or subtract charge delion (iion)
    if (delion < 0) {
        // addition of electron charge
        iion = iscr;
        // except special cases
        if (ihole != 0 && iscr > 0 &&
            iocc[index0][iscr - 1] + 1 - delion > 2 * std::abs(kappa[iscr - 1])) {
            iion = ilast;
            if (ilast == iscr ||
                (ilast > 0 && iocc[index0][ilast - 1] - delion > 2 * std::abs(kappa[ilast - 1]))) {
                iion = ilast + 1;
            }
        }
    }

    // iorb array indexed by kappa value (-4..3)
    // We use a local array with offset: iorb_k[k+4] for k = -4..3
    [[maybe_unused]] int iorb_k[8] = {0, 0, 0, 0, 0, 0, 0, 0};

    norb = 0;
    for (int j = 0; j < 29; ++j) {
        int i1 = j + 1;  // 1-based Fortran orbital index
        if (iocc[index0][j] > 0 ||
            (i1 == iscr && ihole > 0) ||
            (i1 == iion && iocc[index0][j] - delion > 0)) {

            if (i1 != ihole || iocc[index0][j] >= 1) {
                norb++;
                int n = norb;  // 1-based output index
                nqn_out[n - 1] = nnum[j];
                nk_out[n - 1] = kappa[j];
                xnel_out[n - 1] = iocc[index0][j];

                if (i1 == ihole) {
                    xnel_out[n - 1] -= 1;
                    iholep = norb;
                }
                if (i1 == iscr && ihole > 0) {
                    xnel_out[n - 1] += 1;
                }

                xnval_out[n - 1] = ival[index0][j];
                if ((kappa[j] == -4 || kappa[j] == 3) && iunf == 0) {
                    xnval_out[n - 1] = 0;
                }
                xmag_out[n - 1] = ispn[index0][j];

                iorb_k[nk_out[n - 1] + 4] = norb;

                if (i1 == ihole && xnval_out[n - 1] >= 1) {
                    xnval_out[n - 1] -= 1;
                }
                if (i1 == iscr && ihole > 0) {
                    xnval_out[n - 1] += 1;
                }
                if (i1 == iion) {
                    xnel_out[n - 1] -= delion;
                    xnval_out[n - 1] -= delion;
                }
            }
        }
    }
    norbco = norb;

    // Check that all occupation numbers are within limits
    for (int i = 0; i < norb; ++i) {
        double maxocc = 2.0 * std::abs(nk_out[i]);
        if (xnel_out[i] < 0 || xnel_out[i] > maxocc ||
            xnval_out[i] < 0 || xnval_out[i] > maxocc) {
            std::ostringstream oss;
            oss << " error in getorb. Check occupation number for "
                << (i + 1) << "-th orbital. May be a problem with ionicity.";
            logger().wlog(oss.str());
            feff::par::par_stop("GETORB-99");
        }
    }

    // Set iorb_out to the last occupied orbital index
    // The Fortran code returns iorb as the value of the last iorb(nk) assignment
    // which corresponds to the last orbital's kappa-based lookup.
    // The caller uses iorb as the index of the last occupied orbital for projections.
    iorb_out = norb;
}

